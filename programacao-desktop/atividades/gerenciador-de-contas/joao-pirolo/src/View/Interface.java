    private void jButtonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIgualActionPerformed
        // TODO add your handling code here:
        //operaçoes aritmeticas
        value2 = Double.parseDouble(txtResult.getText());
        if(symbol == "sum"){
            txtResult.setText(String.valueOf(value1 + value2));
        }
        if(symbol == "subtraction"){
            txtResult.setText(String.valueOf(value1 - value2));
        }
        if(symbol == "multiplication"){
            txtResult.setText(String.valueOf(value1 * value2));
        }
        if(symbol == "division"){
            txtResult.setText(String.valueOf(value1 / value2));
        }
        if(symbol == "percent"){
            txtResult.setText(String.valueOf(value1 * value2/100));
        }
    }//GEN-LAST:event_jButtonIgualActionPerformed

