    private void jButtonPlusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonPlusMouseClicked
        // TODO add your handling code here:
        // first operator click
        if (operator == 0){
            operator = '+';
            value1 = screenCalc.getText();
        }
        
        // do nothing, two operators in sequence
        else if(value.equals(""))
            operator = '+'; 
        
        // get result from last expression before start a new one
        else{
            value2 = screenCalc.getText();
            float valueAux = methods.equal(Float.parseFloat(value1), Float.parseFloat(value2), operator);
            clear();
            value1 = Float.toString(valueAux);
            operator = '+';
            screenCalc.setText(value1);
        }
        value="";
    }//GEN-LAST:event_jButtonPlusMouseClicked

    private void jButtonEqualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonEqualMouseClicked
        // TODO add your handling code here:
        
        // print result
        if(!value1.equals("0") && !value.equals("")){
            value2 = screenCalc.getText();
            float valueAux = methods.equal(Float.parseFloat(value1), Float.parseFloat(value2), operator);
            clear();
            value1 = Float.toString(valueAux);
            screenCalc.setText(value1);
        }
        
        // do nothing, values not set
        value="";
    }//GEN-LAST:event_jButtonEqualMouseClicked

