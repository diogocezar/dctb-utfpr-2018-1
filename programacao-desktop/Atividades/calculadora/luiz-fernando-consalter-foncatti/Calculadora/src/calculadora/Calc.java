    public void fazOperacao(double n1, double n2, String s){
        valor2 = Double.parseDouble(txtResultado.getText());
        double valorFinal = 0;
        if (null != sinal)switch (sinal) {
            case "+":
                valorFinal = valor1 + valor2;
                break;
            case "-":
                valorFinal = valor1 - valor2;
                break;
            case "X":
                valorFinal = valor1 * valor2;
                break;
            case "/":
                valorFinal = valor1/valor2;
                break;
            default:
                break;
        }
        txtResultado.setText(String.valueOf(valorFinal));
    }

